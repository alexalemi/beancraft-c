// ---------------------------------------------------------------------------
// Growable text buffer
// ---------------------------------------------------------------------------

typedef struct {
    char *data;
    size_t len;
    size_t cap;
} QbeBuffer;

static void buf_init(QbeBuffer *buf) {
    buf->cap = 4096;
    buf->data = malloc(buf->cap);
    buf->len = 0;
    buf->data[0] = '\0';
}

static void buf_free(QbeBuffer *buf) {
    free(buf->data);
}

static void buf_ensure(QbeBuffer *buf, size_t need) {
    if (buf->len + need >= buf->cap) {
        while (buf->len + need >= buf->cap) {
            buf->cap *= 2;
        }
        buf->data = realloc(buf->data, buf->cap);
    }
}

static void buf_printf(QbeBuffer *buf, const char *fmt, ...) {
    va_list args;
    va_start(args, fmt);

    va_list args_copy;
    va_copy(args_copy, args);
    int needed = vsnprintf(NULL, 0, fmt, args_copy);
    va_end(args_copy);

    if (needed < 0) {
        va_end(args);
        return;
    }

    buf_ensure(buf, (size_t)needed + 1);
    buf->len += (size_t)vsnprintf(buf->data + buf->len, buf->cap - buf->len, fmt, args);
    va_end(args);
}

static void buf_puts(QbeBuffer *buf, const char *s) {
    size_t len = strlen(s);
    buf_ensure(buf, len + 1);
    memcpy(buf->data + buf->len, s, len + 1);
    buf->len += len;
}

// ---------------------------------------------------------------------------
// Shared QBE fragments (used by both the plain and the optimized backend)
// ---------------------------------------------------------------------------

// Header comment + the global register storage array.
static void emit_header(QbeBuffer *buf, uint32_t reg_count, bool optimized) {
    buf_printf(buf, "# Generated by beancraft-c QBE backend%s\n",
               optimized ? " (optimized)" : "");
    buf_puts(buf, "# Each register is a Bignum (pointer-tagged 64-bit word);\n");
    buf_puts(buf, "# the runtime functions bc_inc/bc_dec/bc_zero/bc_transfer operate on it.\n\n");
    buf_puts(buf, "# Register storage\n");
    buf_printf(buf, "export data $bc_regs = align 8 { z %u }\n\n",
               (unsigned)(reg_count * 8));
}

// Begin the @inst_<i> basic block: bump the step counter and bail to @exit if
// it reached the limit, otherwise fall through to @inst_<i>_body.
static void emit_step_check(QbeBuffer *buf, uint32_t i) {
    buf_printf(buf, "@inst_%u\n", i);
    buf_puts(buf, "    %steps =l add %steps, 1\n");
    buf_puts(buf, "    %limit_check =w cugel %steps, %max_steps\n");
    buf_printf(buf, "    jnz %%limit_check, @exit, @inst_%u_body\n", i);
    buf_printf(buf, "@inst_%u_body\n", i);
}

// Compute %ptr = &bc_regs[reg].
static void emit_reg_ptr(QbeBuffer *buf, const char *name, uint32_t reg) {
    buf_printf(buf, "    %%%s =l add $bc_regs, %u\n", name, reg * 8);
}

// The @exit block and the closing brace of $bc_run.
static void emit_run_footer(QbeBuffer *buf) {
    buf_puts(buf, "@exit\n");
    buf_puts(buf, "    ret %steps\n");
    buf_puts(buf, "}\n\n");
}

// $bc_reg_count plus the register-name table consumed by the driver.
static void emit_reg_metadata(QbeBuffer *buf, Str **reg_names, uint32_t reg_count) {
    buf_puts(buf, "export function l $bc_reg_count() {\n");
    buf_puts(buf, "@start\n");
    buf_printf(buf, "    ret %u\n", reg_count);
    buf_puts(buf, "}\n\n");

    buf_puts(buf, "# Register names\n");
    for (uint32_t i = 0; i < reg_count; i++) {
        buf_printf(buf, "data $bc_regname_%u = { b \"", i);
        for (const char *p = reg_names[i]->data; *p; p++) {
            if (*p == '"' || *p == '\\') {
                buf_printf(buf, "\\%c", *p);
            } else {
                char s[2] = { *p, '\0' };
                buf_puts(buf, s);
            }
        }
        buf_puts(buf, "\", b 0 }\n");
    }
    buf_puts(buf, "\n");

    buf_puts(buf, "export data $bc_reg_names = align 8 {\n");
    for (uint32_t i = 0; i < reg_count; i++) {
        buf_printf(buf, "    l $bc_regname_%u%s\n", i, i + 1 < reg_count ? "," : "");
    }
    buf_puts(buf, "}\n\n");
}

// $bc_set_reg / $bc_get_reg: a 64-bit numeric view of a register, kept for
// callers that want one without going through the bignum runtime.
static void emit_reg_accessors(QbeBuffer *buf) {
    buf_puts(buf, "# Set a register to a small immediate by index\n");
    buf_puts(buf, "export function $bc_set_reg(l %idx, l %value) {\n");
    buf_puts(buf, "@start\n");
    buf_puts(buf, "    %offset =l mul %idx, 8\n");
    buf_puts(buf, "    %ptr =l add $bc_regs, %offset\n");
    buf_puts(buf, "    %shifted =l shl %value, 1\n");
    buf_puts(buf, "    %tagged =l or %shifted, 1\n");
    buf_puts(buf, "    storel %tagged, %ptr\n");
    buf_puts(buf, "    ret\n");
    buf_puts(buf, "}\n\n");

    buf_puts(buf, "# Read a register by index (exact value if it fits in 64 bits)\n");
    buf_puts(buf, "export function l $bc_get_reg(l %idx) {\n");
    buf_puts(buf, "@start\n");
    buf_puts(buf, "    %offset =l mul %idx, 8\n");
    buf_puts(buf, "    %ptr =l add $bc_regs, %offset\n");
    buf_puts(buf, "    %tagged =l loadl %ptr\n");
    buf_puts(buf, "    %is_imm =l and %tagged, 1\n");
    buf_puts(buf, "    jnz %is_imm, @immediate, @heap\n");
    buf_puts(buf, "@immediate\n");
    buf_puts(buf, "    %value =l shr %tagged, 1\n");
    buf_puts(buf, "    ret %value\n");
    buf_puts(buf, "@heap\n");
    buf_puts(buf, "    %val =l call $bc_bignum_to_u64(l %tagged)\n");
    buf_puts(buf, "    ret %val\n");
    buf_puts(buf, "}\n\n");
}

