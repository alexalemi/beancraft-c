BcResult qbe_generate(FILE *out, const IrProgram *prog, QbeOptions opts) {
    QbeBuffer buf;
    buf_init(&buf);

    emit_header(&buf, prog->reg_count, false);

    buf_puts(&buf, "# Run the program; returns the number of steps executed.\n");
    buf_puts(&buf, "export function l $bc_run(l %max_steps) {\n");
    buf_puts(&buf, "@entry\n");
    buf_puts(&buf, "    %steps =l copy 0\n");
    buf_puts(&buf, "    jmp @inst_0\n\n");

    for (uint32_t i = 0; i < prog->inst_count; i++) {
        const IrInst *inst = &prog->insts[i];

        if (opts.emit_debug_info && prog->label_names && prog->label_names[i]) {
            buf_printf(&buf, "# %s\n", prog->label_names[i]->data);
        }
        emit_step_check(&buf, i);

        switch (inst->op) {
        case IR_INC:
            emit_reg_ptr(&buf, "ptr", inst->reg);
            buf_puts(&buf, "    call $bc_inc(l %ptr)\n");
            buf_printf(&buf, "    jmp @inst_%u\n\n", inst->arg_a);
            break;
        case IR_DEB:
            emit_reg_ptr(&buf, "ptr", inst->reg);
            buf_puts(&buf, "    %was_positive =w call $bc_dec(l %ptr)\n");
            buf_printf(&buf, "    jnz %%was_positive, @inst_%u, @inst_%u\n\n",
                       inst->arg_b, inst->arg_a);
            break;
        case IR_END:
            buf_puts(&buf, "    jmp @exit\n\n");
            break;
        }
    }

    emit_run_footer(&buf);
    emit_reg_accessors(&buf);
    emit_reg_metadata(&buf, prog->reg_names, prog->reg_count);

    fputs(buf.data, out);
    buf_free(&buf);
    return BC_OK(NULL);
}

BcResult qbe_generate_opt(FILE *out, const IrOptProgram *prog, QbeOptions opts) {
    QbeBuffer buf;
    buf_init(&buf);

    emit_header(&buf, prog->reg_count, true);

    buf_puts(&buf, "# Run the program; returns the number of steps executed.\n");
    buf_puts(&buf, "export function l $bc_run(l %max_steps) {\n");
    buf_puts(&buf, "@entry\n");
    buf_puts(&buf, "    %steps =l copy 0\n");
    buf_puts(&buf, "    jmp @inst_0\n\n");

    for (uint32_t i = 0; i < prog->inst_count; i++) {
        const IrOptInst *inst = &prog->insts[i];
        emit_step_check(&buf, i);

        switch (inst->op) {
        case IR_OPT_INC:
            emit_reg_ptr(&buf, "ptr", inst->reg);
            buf_puts(&buf, "    call $bc_inc(l %ptr)\n");
            buf_printf(&buf, "    jmp @inst_%u\n\n", inst->arg_a);
            break;

        case IR_OPT_DEB:
            emit_reg_ptr(&buf, "ptr", inst->reg);
            buf_puts(&buf, "    %was_positive =w call $bc_dec(l %ptr)\n");
            buf_printf(&buf, "    jnz %%was_positive, @inst_%u, @inst_%u\n\n",
                       inst->arg_b, inst->arg_a);
            break;

        case IR_OPT_END:
            buf_puts(&buf, "    jmp @exit\n\n");
            break;

        case IR_OPT_ZERO:
            if (opts.emit_debug_info)
                buf_printf(&buf, "    # ZERO %s\n", prog->reg_names[inst->reg]->data);
            emit_reg_ptr(&buf, "ptr", inst->reg);
            buf_puts(&buf, "    call $bc_zero(l %ptr)\n");
            buf_printf(&buf, "    jmp @inst_%u\n\n", inst->arg_a);
            break;

        case IR_OPT_TRANSFER:
            if (opts.emit_debug_info) {
                buf_printf(&buf, "    # TRANSFER %s -> {", prog->reg_names[inst->reg]->data);
                for (uint32_t d = 0; d < inst->dest_count; d++)
                    buf_printf(&buf, "%s%s", d ? ", " : "",
                               prog->reg_names[prog->dests[inst->dest_off + d]]->data);
                buf_puts(&buf, "}\n");
            }
            emit_reg_ptr(&buf, "src", inst->reg);
            buf_puts(&buf, "    %srcval =l loadl %src\n");
            for (uint32_t d = 0; d < inst->dest_count; d++) {
                buf_printf(&buf, "    %%dst%u =l add $bc_regs, %u\n",
                           d, prog->dests[inst->dest_off + d] * 8);
                buf_printf(&buf, "    call $bc_add_into(l %%dst%u, l %%srcval)\n", d);
            }
            buf_puts(&buf, "    call $bc_zero(l %src)\n");
            buf_printf(&buf, "    jmp @inst_%u\n\n", inst->arg_a);
            break;

        case IR_OPT_COPY:
            // The optimizer never emits COPY; if it ever does, fall through
            // harmlessly rather than miscompile.
            buf_printf(&buf, "    jmp @inst_%u\n\n", inst->arg_a);
            break;

        default:
            buf_printf(&buf, "    # unknown op %d\n", inst->op);
            buf_puts(&buf, "    jmp @exit\n\n");
            break;
        }
    }

    emit_run_footer(&buf);
    emit_reg_accessors(&buf);
    emit_reg_metadata(&buf, prog->reg_names, prog->reg_count);

    fputs(buf.data, out);
    buf_free(&buf);
    return BC_OK(NULL);
}

BcResult qbe_generate_string(char **out, const IrProgram *prog, QbeOptions opts) {
    char *buf = NULL;
    size_t size = 0;
    FILE *stream = open_memstream(&buf, &size);
    if (!stream) {
        *out = NULL;
        return BC_OK(NULL);
    }

    BcResult result = qbe_generate(stream, prog, opts);
    fclose(stream);

    if (result.ok) {
        *out = buf;
    } else {
        free(buf);
        *out = NULL;
    }
    return result;
}

int qbe_compile(const char *qbe_source, const char *output_path) {
    char temp_path[] = "/tmp/beancraft_XXXXXX.ssa";
    int fd = mkstemps(temp_path, 4);
    if (fd < 0) return -1;

    FILE *f = fdopen(fd, "w");
    if (!f) {
        close(fd);
        return -1;
    }
    fputs(qbe_source, f);
    fclose(f);

    char asm_path[256];
    snprintf(asm_path, sizeof(asm_path), "%s.s", output_path);

    char cmd[512];
    snprintf(cmd, sizeof(cmd), "qbe -o %s %s", asm_path, temp_path);
    int ret = system(cmd);
    unlink(temp_path);
    if (ret != 0) return -1;

    snprintf(cmd, sizeof(cmd), "cc -c -o %s %s", output_path, asm_path);
    ret = system(cmd);
    unlink(asm_path);
    return ret == 0 ? 0 : -1;
}

int qbe_link(const char *object_path, const char *output_path) {
    char cmd[512];
    snprintf(cmd, sizeof(cmd), "cc -o %s %s -L. -lbcruntime", output_path, object_path);
    return system(cmd) == 0 ? 0 : -1;
}
